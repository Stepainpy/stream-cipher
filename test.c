#define test_gamma(name, keysz, skip, key, gamma) do {   \
    unsigned char G[sizeof(gamma) - 1]; size_t i;        \
                                                         \
    printf("- Set key with %3i bits ... ", keysz);       \
    puts(name##_setup_key(key, keysz) == 0 ? OK : FAIL); \
    name##_begin_gen();                                  \
                                                         \
    if (skip > 0) {                                      \
        printf("- Skiping %i bytes\n", skip);            \
        name##_skip_gamma(skip);                         \
    }                                                    \
                                                         \
    printf("- Check gamma           ... ");              \
    name##_take_gamma(G, sizeof G);                      \
    if (memcmp(G, gamma, sizeof G) == 0) puts(OK);       \
    else {                                               \
        puts(FAIL);                                      \
        printf("  > expected: ");                        \
        for (i = 0; i < sizeof G; i++)                   \
            printf("%02x", gamma[i] & 0xFF);             \
        putchar('\n');                                   \
        printf("  > received: ");                        \
        for (i = 0; i < sizeof G; i++)                   \
            printf("%02x", G[i]);                        \
        putchar('\n');                                   \
    }                                                    \
} while (0)

